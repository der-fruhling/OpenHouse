package oh.cmdln.write;

public class Print {
    public static void println(String write) {
        System.out.println(write);
    }
    public static void printlnchar(char write) {
        System.out.println(write);
    }
    public static void print(String write) {
        System.out.print(write);
    }
    public static void printchar(char write) {
        System.out.print(write);
    }
}
